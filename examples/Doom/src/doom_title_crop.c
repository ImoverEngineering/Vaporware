void doom_title_crop_draw(void) {
    static uint16_t row[DOOM_TITLE_CROP_W];  /* 256 bytes SRAM */
    display_fill(COL_BLACK);

    for (uint16_t y = 0; y < DOOM_TITLE_CROP_H; y++) {
        const uint8_t *p = &doom_title_crop_pixels_4bpp[y * (DOOM_TITLE_CROP_W / 2)];

        for (uint16_t x = 0; x < DOOM_TITLE_CROP_W; x += 2) {
            uint8_t b = *p++;
            row[x]     = doom_title_crop_palette[b >> 4];
            row[x + 1] = doom_title_crop_palette[b & 0x0F];
        }

        display_draw_image(row, 0, DOOM_TITLE_CROP_Y + y, DOOM_TITLE_CROP_W, 1);
    }
}
